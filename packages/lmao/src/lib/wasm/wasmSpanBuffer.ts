/**
 * Options for creating a ROOT WASM SpanBuffer instance.
 * Thread ID comes from WASM header, span_id generated by WASM.
 */
export interface WasmSpanBufferRootOptions {
  /** WASM allocator for memory management */
  allocator: WasmAllocator;
  /** Buffer capacity (rows) */
  capacity: number;
  /** Span name (written to row 0 message column) */
  spanName: string;
  /** Trace ID (string UUID) - stored in identity block */
  trace_id: string;
  /** Log schema defining columns */
  logSchema: LogSchema;
}

/**
 * Options for creating a CHILD WASM SpanBuffer instance.
 * Inherits trace_id from parent, span_id generated by WASM.
 */
export interface WasmSpanBufferChildOptions {
  /** Parent buffer to link to */
  parent: WasmSpanBufferInstance;
  /** Span name (written to row 0 message column) */
  spanName: string;
  /** Buffer capacity (rows) - optional, inherits from parent */
  capacity?: number;
}

// Legacy options type for compatibility during migration
export interface WasmSpanBufferOptions {
  allocator: WasmAllocator;
  capacity: number;
  trace_id: string;
  thread_id: bigint;
  span_id: number;
  parent_thread_id?: bigint;
  parent_span_id?: number;
  logSchema: LogSchema;
  _traceRoot: ITraceRoot;
  _scopeValues: Readonly<Record<string, unknown>>;
  _opMetadata: OpMetadata;
  _callsiteMetadata: OpMetadata;
}

/**
 * WASM SpanBuffer instance interface.
 * Mirrors AnySpanBuffer but with WASM-specific internals.
 */
export interface WasmSpanBufferInstance {
  // ===========================================================================
  // Identity (same as JS SpanBuffer)
  // ===========================================================================
  readonly trace_id: string;
  readonly thread_id: bigint;
  readonly span_id: number;
  readonly parent_thread_id: bigint;
  readonly parent_span_id: number;
  readonly _hasParent: boolean;

  // ===========================================================================
  // Capacity and write tracking
  // ===========================================================================
  readonly _capacity: number;
  _writeIndex: number;

  // ===========================================================================
  // Cached WASM function refs (hot path optimization)
  // ===========================================================================
  readonly _writeColF64: (colOffset: number, rowIdx: number, value: number, capacity: number) => number;
  readonly _writeColU32: (colOffset: number, rowIdx: number, value: number, capacity: number) => number;
  readonly _writeColU8: (colOffset: number, rowIdx: number, value: number, capacity: number) => number;
  readonly _writeLogEntry: (
    systemPtr: number,
    identityPtr: number,
    traceRootPtr: number,
    entryType: number,
    capacity: number,
  ) => number;

  // ===========================================================================
  // WASM pointers
  // ===========================================================================
  /** Byte offset into WASM memory for system columns (timestamp + entry_type) */
  readonly _systemPtr: number;
  /** Byte offset into WASM memory for identity block (writeIndex, span_id, trace_id) */
  readonly _identityPtr: number;
  /** Array of byte offsets for each column (-1 = not allocated) */
  readonly _columnPtrs: Int32Array;
  /** Reference to the WASM allocator */
  readonly _allocator: WasmAllocator;

  // ===========================================================================
  // Schema
  // ===========================================================================
  readonly _logSchema: LogSchema;

  // ===========================================================================
  // Tree structure
  // ===========================================================================
  _parent: WasmSpanBufferInstance | null;
  _children: WasmSpanBufferInstance[];
  _overflow: WasmSpanBufferInstance | null;

  // ===========================================================================
  // System column views (created on demand from WASM memory)
  // ===========================================================================
  get timestamp(): BigInt64Array;
  get entry_type(): Uint8Array;

  // ===========================================================================
  // Message column (system, eager string)
  // ===========================================================================
  readonly _message: string[];
  message(idx: number, value: string): this;

  // ===========================================================================
  // Methods
  // ===========================================================================
  /** Free all WASM memory for this buffer */
  free(): void;

  /** Check if column is allocated */
  isColumnAllocated(columnIndex: number): boolean;

  /** Get column values array if allocated */
  getColumnIfAllocated(columnName: string): unknown;

  /** Get column nulls array if allocated */
  getNullsIfAllocated(columnName: string): Uint8Array | undefined;
}

/**
 * Constructor type for generated WASM SpanBuffer classes.
 */
export interface WasmSpanBufferConstructor {
  new (opts: WasmSpanBufferOptions): WasmSpanBufferInstance;
  readonly schema: LogSchema;
  stats: SpanBufferStats; // Mutable stats shared across all instances
}

